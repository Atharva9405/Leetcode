class Solution{
    public:
    // n: input to count the number of set bits
    //Function to return sum of count of set bits in the integers from 1 to n.
    int countSetBits(int n)
    {
        n++;
        int count=0;
        for( int x=2 ; x/2<n ; x=x*2 ){
            int quotient = n/x;
            count += quotient * x / 2;
            int remainder = n%x;
            if(remainder > x/2){
                count += remainder - x/2;
            }
        }
        return count;
    }
};


